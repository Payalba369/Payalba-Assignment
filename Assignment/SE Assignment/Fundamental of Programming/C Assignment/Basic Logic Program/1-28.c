


// Que.28	Convert years into days and months
#include <stdio.h>
void main()
{
    int days, year,month;
    printf("Enter the number of days\n");
    scanf("%d", &year);
    days= year*365;
    month= year*12;
    printf ("%d year is equal to %d month and  %d  days", year, month,  days);
}

















