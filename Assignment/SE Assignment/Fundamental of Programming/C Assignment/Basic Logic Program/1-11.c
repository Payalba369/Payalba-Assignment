


//Que.11 Find circumference of square formula : C = 4 * a
#include <stdio.h>

int main() {
    float sideLen, perimeter;

    printf("Enter the side length of the square: ");
    scanf("%f", &sideLen);

    perimeter = 4 * sideLen;

    printf("The perimeter of the square is: %.2f\n", perimeter);

    return 0;
}




